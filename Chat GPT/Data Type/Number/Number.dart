void main() {
  // Number -> Integer -> double -> num
  int age = 20; // Integer
  double price = 599.99; // double
  num total = age + price; // num
  print(age); // print 20
  print(price); // print 599.99
  print(total); // print 619.99
}

/*
Explanation

int age = 20; → integer value store ہوتی ہے۔

double price = 99.99; → decimal value store ہوتی ہے۔

num total = age + price; → num ایک parent type ہے جو int اور double دونوں کو رکھ سکتا ہے۔
*/
